#include <iostream>
using namespace std;

int main() {
    int a = 11, b = 5;

    // Arithematic Operator
    cout << "Sum = " << a + b << endl;
    cout << "Difference = " << a - b << endl;
    cout << "Product = " << a * b << endl;
    cout << "Division = " << a / b << endl;
    cout << "Modulo [Remainder] = " << a % b << endl;

    return 0;
}